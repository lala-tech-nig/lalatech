const groups = rawText.split(/\n\s*\n/);
let tools = [];

groups.forEach(group => {
    const lines = group.trim().split('\n');
    if (lines.length === 3) {
        if (lines[0].toLowerCase() === 'search') return;
        
        let slug = lines[0].toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '');
        tools.push({
            name: lines[0].trim(),
            category: lines[1].trim(),
            desc: lines[2].trim(),
            slug: slug
        });
    }
});

// Avoid duplicates
const uniqueTools = [];
const seenSlugs = new Set();
for (const tool of tools) {
    if (!seenSlugs.has(tool.slug)) {
        seenSlugs.add(tool.slug);
        uniqueTools.push(tool);
    }
}

const fileContent = `export const toolsList = ${JSON.stringify(uniqueTools, null, 2)};\n`;
const fs = require('fs');
fs.writeFileSync('C:\\Users\\LALA TECH\\Desktop\\lalatech\\client\\lib\\toolsList.js', fileContent);
console.log('Successfully generated toolsList.js with', uniqueTools.length, 'tools');
